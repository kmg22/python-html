# 문자열 데이터 타입

print('Hello world')
print("Hello world")

# 여러 줄에 걸쳐 출력
print('''
      Hello
      world
      ''')
#
#      Hello
#      world
#

print("'1'+'1'=", '1'+'1') # '1'+'1'= 11
print('Hello world'*100)
print("len('Hello world'*100)=", len('Hello world'*100)) # len('Hello world'*100)= 1100
print("'Hello world'.replace('world', 'universe')=", 'Hello world'.replace('world', 'universe')) # 'Hello world'.replace('world', 'universe')= Hello universe